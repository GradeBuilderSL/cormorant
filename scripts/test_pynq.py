#!/usr/bin/env python3
"""
PYNQ hardware test for VectorOPKernel (ap_fixed<16,8>)

Mirrors the vadd_tb.sv stimulus / verification sequence:
  - Generator : create transaction (op, size, A fill, B fill, expected C)
  - Driver    : allocate CMA buffers, fill A/B, poison C, write registers, ap_start
  - Monitor   : poll ap_done, service ISR
  - Scoreboard: verify every C element equals the expected value

Register map: xvectoropkernel_hw.h (base 0xA000_0000)
  Verify REG_OP offset from the generated driver header after synthesis.
Data type   : ap_fixed<16,8>  — 1.0=0x0100, 2.0=0x0200, 3.0=0x0300

Usage (on KV260 PYNQ):
    sudo python3 test_pynq.py              # all operations, default size=256
    sudo python3 test_pynq.py --size 1024  # all operations, custom size
    sudo python3 test_pynq.py --no-load    # skip bitstream load
"""

