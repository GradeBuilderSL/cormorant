class TestConvKernelRegistry(unittest.TestCase):
    """Verify ConvKernel is present in KERNEL_REGISTRY with correct metadata."""

    def test_conv_entry_exists(self):
        self.assertIn("ConvKernel", KERNEL_REGISTRY)

    def test_driver_prefix(self):
        self.assertEqual(KERNEL_REGISTRY["ConvKernel"].driver_prefix, "xconvkernel")

    def test_c_type(self):
        self.assertEqual(KERNEL_REGISTRY["ConvKernel"].c_type, "XConvkernel")

    def test_uio_default(self):
        self.assertEqual(KERNEL_REGISTRY["ConvKernel"].uio_default, "ConvKernel_0")

    def test_axi_base(self):
        self.assertEqual(KERNEL_REGISTRY["ConvKernel"].axi_base, 0xA002_0000)

    def test_c_var(self):
        self.assertEqual(KERNEL_REGISTRY["ConvKernel"].c_var, "s_convkernel")

    def test_init_param(self):
        self.assertEqual(KERNEL_REGISTRY["ConvKernel"].init_param, "convkernel_instance")

    def test_instance_macro(self):
        self.assertEqual(
            KERNEL_REGISTRY["ConvKernel"].instance_macro,
            "INFERENCE_CONVKERNEL_INSTANCE",
        )

    def test_driver_files_present(self):
        files = KERNEL_REGISTRY["ConvKernel"].driver_files
        for f in ("xconvkernel.h", "xconvkernel_hw.h",
                  "xconvkernel.c", "xconvkernel_sinit.c", "xconvkernel_linux.c"):
            self.assertIn(f, files)

    def test_registry_order_vop_mm_conv(self):
        """VectorOPKernel precedes MatmulKernel precedes ConvKernel in registry."""
        names = list(KERNEL_REGISTRY.keys())
        self.assertLess(names.index("VectorOPKernel"), names.index("MatmulKernel"))
        self.assertLess(names.index("MatmulKernel"),   names.index("ConvKernel"))


