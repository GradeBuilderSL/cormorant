@unittest.skipUnless(_all_models_exist(),
                     "Run test/gen_mixed_all_kernels_models.py first")
class TestMixedAllKernelsLayouts(unittest.TestCase):
    """All tensors in all-kernel models have flat layouts (gap=0, n_chunks=1).

    ConvNode outputs are NCHW-flat; VectorOP nodes upstream of Conv accept
    flat inputs; no broadcast VectorOP is present in any of these models.
    """

    def _gen(self, name: str) -> CodeGenerator:
        return _gen(name)

    def _assert_flat(self, gen, name):
        lay = gen._layouts[name]
        self.assertEqual(lay.n_chunks, 1, f"{name}: n_chunks should be 1")
        self.assertEqual(lay.gap,      0, f"{name}: gap should be 0")

    def test_relu_conv_matmul_all_flat(self):
        g = self._gen("mixed_all_relu_conv_matmul.onnx")
        for t in ("Z", "A", "B", "Y"):
            self._assert_flat(g, t)

    def test_conv_matmul_relu_all_flat(self):
        g = self._gen("mixed_all_conv_matmul_relu.onnx")
        for t in ("Z", "A", "B", "Y"):
            self._assert_flat(g, t)

    def test_norm_conv_project_all_flat(self):
        g = self._gen("mixed_all_norm_conv_project.onnx")
        for t in ("Z1", "Z2", "A", "B", "Y"):
            self._assert_flat(g, t)

    def test_matmul_relu_conv_all_flat(self):
        g = self._gen("mixed_all_matmul_relu_conv.onnx")
        for t in ("Z", "A", "B", "Y"):
            self._assert_flat(g, t)

    def test_skip_conv_matmul_all_flat(self):
        g = self._gen("mixed_all_skip_conv_matmul.onnx")
        for t in ("Z", "A", "B", "Y"):
            self._assert_flat(g, t)

    def test_two_conv_matmul_relu6_all_flat(self):
        g = self._gen("mixed_all_two_conv_matmul_relu6.onnx")
        for t in ("Z1", "A1", "Z2", "A2", "B", "Y"):
            self._assert_flat(g, t)

    def test_sub_matmul_conv_all_flat(self):
        g = self._gen("mixed_all_sub_matmul_conv.onnx")
        for t in ("Z1", "Z2", "A", "B", "Y"):
            self._assert_flat(g, t)

    def test_no_broadcast_io_map_entries(self):
        """No model has broadcast VectorOP → _broadcast_io_map() is empty."""
        for name in [
            "mixed_all_relu_conv_matmul.onnx",
            "mixed_all_conv_matmul_relu.onnx",
            "mixed_all_norm_conv_project.onnx",
            "mixed_all_matmul_relu_conv.onnx",
            "mixed_all_skip_conv_matmul.onnx",
            "mixed_all_two_conv_matmul_relu6.onnx",
            "mixed_all_sub_matmul_conv.onnx",
        ]:
            with self.subTest(model=name):
                bmap = _gen(name)._broadcast_io_map()
                self.assertEqual(bmap, {}, f"{name}: broadcast_io_map should be empty")

    def test_matmul_natural_calls_no_strides(self):
        """No broadcast downstream of any MatMul → all run_matmul strides are natural."""
        for name in [
            "mixed_all_relu_conv_matmul.onnx",
            "mixed_all_conv_matmul_relu.onnx",
            "mixed_all_norm_conv_project.onnx",
            "mixed_all_matmul_relu_conv.onnx",
            "mixed_all_skip_conv_matmul.onnx",
            "mixed_all_two_conv_matmul_relu6.onnx",
            "mixed_all_sub_matmul_conv.onnx",
        ]:
            with self.subTest(model=name):
                src = _gen(name).generate_source()
                # no row-strided decomposition: 4th arg to run_matmul != 1u for batch
                # simplest check: run_matmul_at() is never emitted
                self.assertNotIn("run_matmul_at(", src, f"{name}: no outer loop expected")


