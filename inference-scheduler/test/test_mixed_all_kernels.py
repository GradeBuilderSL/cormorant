"""Tests for models that use all three kernels: VectorOPKernel, MatmulKernel, ConvKernel.

Models generated by test/gen_mixed_all_kernels_models.py:
  1  mixed_all_relu_conv_matmul      Relu → Conv → Relu → MatMul
  2  mixed_all_conv_matmul_relu      Conv(+bias) → Relu → MatMul → Relu
  3  mixed_all_norm_conv_project     Sub → Mul → Conv → Relu → MatMul
  4  mixed_all_matmul_relu_conv      MatMul → Relu → Conv → Relu6
  5  mixed_all_skip_conv_matmul      Conv → Relu → Add(skip) → MatMul
  6  mixed_all_two_conv_matmul_relu6 Conv(+bias) → Relu → Conv → Relu → MatMul → Relu6
  7  mixed_all_sub_matmul_conv       Sub → MatMul → Relu → Conv → Relu6
"""

import os
import sys
import unittest

sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))

from src.graph   import OnnxGraph
from src.codegen import CodeGenerator
from src.nodes   import ConvNode, MatmulNode, ScheduledNode
from src.kernels import KERNEL_REGISTRY


_MODELS_DIR = os.path.join(os.path.dirname(__file__), "models")
_SENTINEL   = "mixed_all_relu_conv_matmul.onnx"


def _all_models_exist() -> bool:
    return os.path.isfile(os.path.join(_MODELS_DIR, _SENTINEL))


def _path(name: str) -> str:
    return os.path.join(_MODELS_DIR, name)


def _gen(name: str) -> CodeGenerator:
    p = _path(name)
    return CodeGenerator(OnnxGraph(p), model_path=p)


