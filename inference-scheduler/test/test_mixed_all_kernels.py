        relu6_pos  = b.find("VECTOROP_RELU6")
        self.assertLess(sub_pos,    matmul_pos)
        self.assertLess(matmul_pos, relu_pos)
        self.assertLess(relu_pos,   conv_pos)
        self.assertLess(conv_pos,   relu6_pos)

