# ------------------------------------------------------------------ #
# Formatting helpers                                                  #
# ------------------------------------------------------------------ #

def _banner(title: str) -> str:
    bar = "/" * 72
    return f"\n{bar}\n/* {title:<68} */\n{bar}\n"


def _file_banner(filename: str, graph: OnnxGraph, model_path: str) -> str:
    ts         = datetime.datetime.now(datetime.timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    model_name = os.path.basename(model_path)
    n_nodes    = len(graph.nodes)
    ops_used   = sorted({OP_NAMES[sn.op_code] for sn in graph.nodes})
    ops_str    = ", ".join(ops_used) if ops_used else "(none)"

    lines = [
        "/*",
        f" * {filename}",
        f" * Auto-generated by inference-scheduler",
        f" * Source model : {model_name}",
        f" * Generated at : {ts}",
        f" * Scheduled ops: {n_nodes}",
        f" * Op codes used: {ops_str}",
        " *",
    ]
    for t in graph.input_tensors:
        lines.append(f" * Input  : {t.onnx_name}  shape={t.shape}  numel={t.numel}")
    for t in graph.output_tensors:
        lines.append(f" * Output : {t.onnx_name}  shape={t.shape}  numel={t.numel}")
    lines += [
        " *",
        " * Target: Xilinx KV260 bare-metal, ap_fixed<16,8> data type",
        " * Kernel: VectorOPKernel (element-wise, 1-D, II=1)",
        " */",
    ]
    return "\n".join(lines)


# ------------------------------------------------------------------ #
# CodeGenerator                                                       #
# ------------------------------------------------------------------ #

class CodeGenerator:
    def __init__(self, graph: OnnxGraph, model_path: str,
                 embed_large_weights: bool = False) -> None:
        self._graph               = graph
        self._model_path          = model_path
        self._embed_large_weights = embed_large_weights

    # ---------------------------------------------------------------- #
    # Public entry points                                               #
    # ---------------------------------------------------------------- #

    def generate_header(self) -> str:
        """Content for include/inference.h."""
        parts = [
            _file_banner("inference.h", self._graph, self._model_path),
            self._header_guard_open(),
            self._header_api(),
            self._header_guard_close(),
        ]
        return "\n".join(parts) + "\n"

    def generate_source(self) -> str:
        """Content for src/inference.c."""
        parts = [
            _file_banner("inference.c", self._graph, self._model_path),
            self._source_includes(),
            self._source_op_defines(),
            self._weight_arrays(),
            self._buffer_declarations(),
            self._kernel_instance(),
            self._run_op_helper(),
            self._init_function(),
            self._inference_function(),
        ]
        return "\n".join(parts) + "\n"

    def generate_buf_impl(self) -> str:
        """Content for src/inference_buf.c.

        Platform-independent public API (accessors) plus two platform
        implementations selected at compile time by __linux__:
          - Linux:      posix_memalign + mlock + /proc/self/pagemap (root).
                        No special device needed; each buffer is independent.
          - Bare-metal: malloc (virtual address == physical address on Xilinx
                        standalone/FreeRTOS BSP).
        """
        return (
            _file_banner("inference_buf.c", self._graph, self._model_path) +
            _BUF_IMPL_TEMPLATE
        )

    def generate_setup_script(self) -> str:
        """Content for scripts/check_inference_setup.sh."""
        pool_bytes = self._compute_pool_bytes()
        model_name = os.path.basename(self._model_path)
        return _SETUP_SCRIPT_TEMPLATE.format(
            model_name=model_name,
            pool_bytes=pool_bytes,
        )

    def generate_test(self) -> str:
        """Content for test/test_inference.c — one-shot smoke test.

        Allocates input/output DMA buffers, fills inputs with a ramp
        (element i = i/256.0 in ap_fixed<16,8> encoding), runs inference,
        and prints the first 8 output values.  Exit code 0 = success.
        """
        graph   = self._graph
        inputs  = graph.input_tensors
        outputs = graph.output_tensors

        # Variable declarations at top of main()
        decl_lines = []
        for t in inputs + outputs:
            decl_lines.append(f"    inference_buf_t *{t.c_name} = NULL;")

        # Buffer allocations with goto-cleanup error handling.
        # Each failure jumps to 'cleanup' which frees all I/O buffers
        # (NULL-safe) and calls inference_deinit(), so no buffer leaks.
        alloc_lines = []
        for t in inputs + outputs:
            macro = f"INFERENCE_{t.c_name.upper()}_SIZE"
            alloc_lines.append(f"    {t.c_name} = inference_buf_alloc({macro});")
            alloc_lines.append(f"    if (!{t.c_name}) {{")
            alloc_lines.append(
                f"        fprintf(stderr, "
                f"\"inference_buf_alloc failed for '{t.c_name}'\\n\");"
            )
            alloc_lines.append("        rc = 1; goto cleanup;")
            alloc_lines.append("    }")

        # Fill each input with a ramp: raw uint16 value i = i/256.0
        fill_lines = []
        for t in inputs:
            macro = f"INFERENCE_{t.c_name.upper()}_SIZE"
            fill_lines += [
                f"    {{",
                f"        Data_t *p = inference_buf_ptr({t.c_name});",
                f"        for (i = 0; i < {macro}; i++)",
                f"            p[i] = (Data_t)(i & 0xFFFFu); /* i/256.0 */",
                f"    }}",
            ]

        # inference_run() call
        run_args = ", ".join(t.c_name for t in inputs + outputs)

        # Print first ≤8 elements of each output
        print_lines = []
        for t in outputs:
            macro = f"INFERENCE_{t.c_name.upper()}_SIZE"
            print_lines += [
                f"    {{",
                f"        Data_t   *p   = inference_buf_ptr({t.c_name});",
                f"        unsigned  lim = ({macro} < 8u) ? {macro} : 8u;",
                f"        printf(\"Output '{t.onnx_name}'"
                f" (%u elem, first %u):\\n\", (unsigned){macro}, lim);",
                f"        for (i = 0; i < lim; i++) {{",
                f"            int16_t s = (int16_t)p[i];",
                f"            printf(\"  [%u] 0x%04X  (%.4f)\\n\",",
                f"                   i, (unsigned)p[i], (double)s / 256.0);",
                f"        }}",
                f"    }}",
            ]

        # Cleanup label: free all I/O buffers (NULL-safe), then deinit.
        cleanup_lines = []
        for t in inputs + outputs:
            cleanup_lines.append(f"    inference_buf_free({t.c_name});")

        decl_str    = "\n".join(decl_lines)
        alloc_str   = "\n".join(alloc_lines)
        fill_str    = "\n".join(fill_lines) if fill_lines else "    /* (no inputs) */"
        print_str   = "\n".join(print_lines)
        cleanup_str = "\n".join(cleanup_lines)

        return (
            _file_banner("test_inference.c", graph, self._model_path) +
            "\n"
            "#include <stdio.h>\n"
            "#include \"inference.h\"\n"
            "\n"
            "/*\n"
            " * Default hardware instance passed to inference_init().\n"
            " *   Linux:      UIO device (see /sys/class/uio/ for the right index)\n"
            " *   Bare-metal: device name string from xparameters.h\n"
            " * Override: cmake -DINFERENCE_TEST_INSTANCE=\\\"/dev/uio1\\\"\n"
            " *       or: ./test_inference /dev/uio1\n"
            " */\n"
            "#ifndef INFERENCE_TEST_INSTANCE\n"
            "#  ifdef __linux__\n"
            "#    define INFERENCE_TEST_INSTANCE  \"/dev/uio0\"\n"
            "#  else\n"
            "#    define INFERENCE_TEST_INSTANCE  \"VectorOPKernel\"\n"
            "#  endif\n"
            "#endif\n"
            "\n"
            "int main(int argc, char **argv)\n"
            "{\n"
            "    const char *instance = (argc > 1) ? argv[1]\n"
            "                                      : INFERENCE_TEST_INSTANCE;\n"
            f"{decl_str}\n"
            "    unsigned i;\n"
            "    int      rc = 0;\n"
            "\n"
            "    /* 1. Initialise: open UIO device, map DMA pool */\n"
            "    rc = inference_init(instance);\n"
            "    if (rc != 0) {\n"
            "        fprintf(stderr, \"inference_init(\\\"%s\\\") failed: %d\\n\",\n"
            "                instance, rc);\n"
            "        return 1;\n"
            "    }\n"
            "    printf(\"inference_init OK (\\\"%s\\\")\\n\", instance);\n"
            "\n"
            "    /* 2. Allocate I/O buffers from the DMA pool */\n"
            f"{alloc_str}\n"
            "\n"
            "    /* 3. Fill inputs: ramp pattern (element i = i/256.0) */\n"
            f"{fill_str}\n"
            "\n"
            "    /* 4. Run inference */\n"
            f"    inference_run({run_args});\n"
            "\n"
            "    /* 5. Print first 8 output elements */\n"
            f"{print_str}\n"
            "\n"
            "    if (rc == 0)\n"
            "        printf(\"test_inference PASSED\\n\");\n"
            "\n"
            "    /* 6. Teardown — reached on both success and alloc failure */\n"
            "cleanup:\n"
            f"{cleanup_str}\n"
            "    inference_deinit();\n"
            "    return rc;\n"
            "}\n"
        )

