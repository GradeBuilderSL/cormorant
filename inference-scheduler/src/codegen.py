            " *   op    VECTOROP_* constant\n"
            " */\n"
            "static void run_op(\n"
            "    inference_buf_t *a,\n"
            "    inference_buf_t *b,\n"
            "    inference_buf_t *c,\n"
            "    unsigned         size,\n"
            "    unsigned         op)\n"
            "{\n"
            "    XVectoropkernel_Set_a(&s_kernel, inference_buf_phys(a));\n"
