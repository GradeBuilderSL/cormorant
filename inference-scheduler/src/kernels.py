"""
Central registry of hardware kernels.

To add a new kernel:
  1. Append a KernelDesc entry to KERNEL_REGISTRY (this file).
  2. Implement a KernelNode subclass in nodes.py with matching kernel_name.

Everything else — driver file selection, CMakeLists, inference_init() signature,
UIO device name macros — is derived automatically from the registry.
"""

from __future__ import annotations
from dataclasses import dataclass
from typing import List, Tuple


__all__ = ["KernelDesc", "KERNEL_REGISTRY"]


@dataclass(frozen=True)
class KernelDesc:
    """Describes a single hardware accelerator kernel."""

    name: str          # "VectorOPKernel"    — matches node.kernel_name
    driver_prefix: str # "xvectoropkernel"   — lowercase filename prefix
    c_type: str        # "XVectoropkernel"   — C struct type from driver header
    uio_default: str   # "VectorOPKernel_0"  — UIO sysfs name (DT node label)
    axi_base: int      # 0xA000_0000         — AXI-Lite ctrl register base

    # Driver source file names (relative, copied into project driver/)
    driver_files: Tuple[str, ...] = ()
    driver_readme: str = ""

    @property
    def c_var(self) -> str:
        """Static C instance variable name, e.g. 's_vectoropkernel'."""
        return f"s_{self.name.lower()}"

    @property
    def init_param(self) -> str:
        """C parameter name for inference_init(), e.g. 'vectoropkernel_instance'."""
        return f"{self.name.lower()}_instance"

    @property
    def instance_macro(self) -> str:
        """CMake / C preprocessor macro for the UIO device name override.
        e.g. 'INFERENCE_VECTOROPKERNEL_INSTANCE'."""
        return f"INFERENCE_{self.name.upper()}_INSTANCE"


