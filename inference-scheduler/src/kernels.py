def all_driver_files() -> List[str]:
    """All driver file names across all registered kernels (for multi-kernel projects)."""
    files: List[str] = []
    seen = set()
    for kd in KERNEL_REGISTRY.values():
        for f in kd.driver_files:
            if f not in seen:
                seen.add(f)
                files.append(f)
    return files


def mixed_driver_readme(kernel_names: List[str]) -> str:
    """Generate a combined driver/README.md for projects using multiple kernels."""
    sections = []
    for name in kernel_names:
        kd = KERNEL_REGISTRY[name]
        sections.append(
            f"## {kd.name} driver\n\n"
            + "\n".join(f"  {f}" for f in kd.driver_files)
            + f"\n\nSource: Vitis HLS synthesis output for {kd.name}.\n"
            f"AXI-Lite base address: 0x{kd.axi_base:08X}\n"
            f"UIO device name:       {kd.uio_default}\n"
        )
    return (
        "# driver/\n\n"
        "This project uses multiple hardware kernels. Place all driver\n"
        "sources from each kernel below before building.\n\n"
        + "\n".join(sections)
    )
